#include "Game.hpp"



/// --------------------------------------------------------------------------------------------------------------
Game::Game()
{
	backGroundDraw = LoadGraph("media\\game.png");
}



/// --------------------------------------------------------------------------------------------------------------
Game::~Game()
{
	GRAPHIC_RELEASE(backGroundDraw);
}



/// --------------------------------------------------------------------------------------------------------------
void Game::Draw()
{
	DrawGraph(0, 0, backGroundDraw, false);
}



/// --------------------------------------------------------------------------------------------------------------
void Game::Process()
{
	if (CheckHitKey(KEY_INPUT_Z) == 1)
	{
		BaseScene::SetScene(ESceneNumber::TITLE);
	}
}
