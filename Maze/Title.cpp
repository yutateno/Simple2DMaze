#include "Title.hpp"



/// --------------------------------------------------------------------------------------------------------------
Title::Title()
{
	backGroundDraw = LoadGraph("media\\title_back.png");
}



/// --------------------------------------------------------------------------------------------------------------
Title::~Title()
{
	GRAPHIC_RELEASE(backGroundDraw);
}



/// --------------------------------------------------------------------------------------------------------------
void Title::Draw()
{
	DrawGraph(0, 0, backGroundDraw, false);
}



/// --------------------------------------------------------------------------------------------------------------
void Title::Process()
{
	if (CheckHitKey(KEY_INPUT_Z) == 1)
	{
		BaseScene::SetScene(ESceneNumber::GAME);
	}
}
